async function addArticle(article) {
  const slug = generateSlug(article.title) + "-" + Date.now();
  
  try {
    const result = await client.execute({
      sql: `INSERT INTO articles (title, slug, content, excerpt, category, image_url, author, featured)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?)`,
      args: [
        article.title,
        slug,
        article.content,
        article.excerpt || null,
        article.category,
        article.image_url || null,
        article.author || null,
        article.featured ? 1 : 0,
      ],
    });
    console.log(`โ Added: ${article.title.substring(0, 50)}... (ID: ${result.lastInsertRowid})`);
    return result.lastInsertRowid;
  } catch (error) {
    console.error(`โ Error: ${article.title.substring(0, 30)}...`, error.message);
    return null;
  }
}

